package primitiveTypecasting;

public class ExplicateCasting {

	public static void main(String args[])
	{
		int a=10;
		short b=(short)a;
		System.out.println("print b");
		
		 ExplicateCasting.modify();
		
	}
	
	public static void modify()
	{
		double x = 34;
		int y= (int)x;
		System.out.println("print  y");
	}
}
